# In[]
for i in range(10):
    print(i)
    
# In[]
for i in range(2,10,2):
    print(i)

# In[]
for i in range(10,1,-2):
    print(i)
    
# In[]
lista = ['Bogotá','Medellín','Barranquilla', 'Cali']
for j in lista:
    print(j)

# In[]
lista = ['Bogotá','Medellín','Barranquilla', 'Cali']
for j in lista[::-1]:
    print(j)

